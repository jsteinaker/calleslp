package com.jsteinaker.calleslp;

public class Resultado {
	private int esquina1, esquina2;

	public Resultado(int esquina1, int esquina2)
	{
		this.esquina1 = esquina1;
		this.esquina2 = esquina2;
	}

	public int getEsquina1()
	{
		return esquina1;
	}

	public int getEsquina2()
	{
		return esquina2;
	}

}
